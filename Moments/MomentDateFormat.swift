import Foundation

enum MomentDateFormat : String{
    
    case day = "EEE"
    case year = "yyyy"
    case month = "MMM"
    case date = "dd"
    case short = "dd/MM/yy"
    case monthAndYear = "MMMM yyyy"
    case dateTime = "dd/MM/yy - h:mma"
    
}
